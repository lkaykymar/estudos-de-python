# Tipos int e float
# int -> Número inteiro: Qualquer número positivo ou negativo.
print(0) # int 
print(89) # int 
print(-45) # int 

# Float -> Número com ponto flutuante(quebrado)
# o tipo float representa qualquer número positivo ou negativo com ponto flutuante
print(1.1) # float
print(-75.46) # float
print(0.0) # float

# A função type mostro o tipo que o python inferiu ao valor.
print(type('Kayky'))
print(type('0'))
print(type(1.1), type(-1.1), type(0.0))

# Se eu colocar aspas em um número ele vira str.
print(type('11'))

