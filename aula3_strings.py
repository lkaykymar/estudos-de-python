"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print('Kayky Martins')
print("Kayky Martins")
''' 
Para imprimir as strings, 
pode-se utilizar ambas as opções,
aspas simples ou aspas duplas
'''
print("Kayky Martins é o \"CR7\" da programação!")
print(r"Kayky Martins é o \"CR7\" da programação!")
print("Kayky Martins é o 'CR7' da programação!")
print('Kayky Martins é o "CR7" da programação!')
'''
Maneiras de utilizar aspas dentro das aspas da str:
1 -> utilizando \ antes das aspas;
2 -> Utilizando aspas simples para a str e aspas duplas para a palavra;
3 -> Utilizando aspas duplas para a str e aspas simples para a palavra.
'''

