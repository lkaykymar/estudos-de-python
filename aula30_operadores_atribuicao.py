'''
Operadores de atribuição

=
+=
-=
*=
/=
//=
**=
%= 
'''
# PRIMEIRO =
contador = 0
contador = contador + 1
print(f'PRIMEIRO: {contador}')

# SEGUNDO +=

contar = 2
contar += 5
print(f'SEGUNDO: {contar}')

# TERCEIRO -=

contando = 2
contando -= 2
print(f'TERCEIRO: {contando}')

# Quarto *=

contand = 2
contand *= 5
print(f'QUARTO: {contand}')

# Quinto /=

contan = 100
contan /= 3
print(f'QUINTO: {contan}')

# SEXTO //=

conta = 100
conta //= 3
print(f'SEXTO: {conta}')

# SÉTIMO

cont = 3
cont **= 3
print(f'SÉTIMO: {cont}')

# OITAVO %=

con = 50
con %= 2
print(f'OITAVO: {con}')

### Vale para str. concatenação

str = "8"
str += '8'
print(f'CONCATENAÇÂO: {str}')

### MULTIPLICANDO STR

string = 'k'
string *= 10
print(f'MULTIPLICAÇÃO: {string}')



